    private void LeftUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LeftUpActionPerformed
        try {
            if (jTextField26.getText().isEmpty() || jTextField27.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Все поля должны быть заполнены", "Ок", JOptionPane.PLAIN_MESSAGE);
            } else
            if (Integer.parseInt(jTextField26.getText()) < 0 || Integer.parseInt(jTextField27.getText()) < 0) {
                throw new NumberFormatException();
            } else {
                colNum.clear();
                for(int i = 0; i < dm.getLastColNumber(selectedFileName, sheetNum); i++){
                    colNum.add(Integer.parseInt(jTextField26.getText())+i);
                }
                for (int i = Integer.parseInt(jTextField27.getText());
                        i <= dm.getLastRowNumber(selectedFileName, sheetNum); i++) {
                    dm.importData(selectedFileName, sheetNum, i, colNum);
                }
                JOptionPane.showMessageDialog(null, "Объекты успешно импортированы!", "Ок", JOptionPane.PLAIN_MESSAGE);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Все поля должны быть заполнены неотрицательными целыми числами", "Ок", JOptionPane.PLAIN_MESSAGE);
        } catch (IOException | InvalidFormatException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_LeftUpActionPerformed

